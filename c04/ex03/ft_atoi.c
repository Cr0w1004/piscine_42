#include <unistd.h>

int	ft_atoi(char *str)
{
	int	res;
	int	segno;
	int	i;

	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
	{
		i++;
	}
	segno = 1;
	while (str[i] == '+' || str[i] == '-' )
	{
		if (str[i] == '-')
		{
			segno *= -1;
		}
		i++;
	}
	res = 0;
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + (str[i] - 48);
		i++;
	}
	return (segno * res);
}
